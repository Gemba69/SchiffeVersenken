        <div class='spielregeln'><h1>Spielregeln</h1>

		<p><strong><FONT SIZE=4>Ziel des Spieles</FONT></strong></p>

		<p>Jeder Spieler muss seine Flotte in Gewässern verstecken und versuchen die
		gegnerische Flotte vollständig zu versenken. Hierbei ist zu beachten, dass die 
		Flotte nicht komplett zum Opfer des Feindfeuers werden darf.<br></p>

		<p><strong><FONT SIZE=4>Setzen der Schiffe</FONT></strong></p>

		<p>Die eigene und die gegnerische Flotte besteht aus:<br><br>
		• 1 Schlachtschiff (5 Treffer nötig zum Versenken)<br>
		• 2 Kreuzer (4 Treffer nötig zum Versenken)<br>
		• 3 Fregatten (3 Treffer nötig zum Versenken)<br>
		• 4 Minensucher (2 Treffer nötig zum Versenken)<br>


		Vor dem ersten Zug muss jeder seine Schiffe einsetzen. Die Schiffe müssen so eingesetzt werden, 
		dass sie sich weder waagerecht noch senkrecht überlappen. Ebenfalls ist ein diagonales setzen nicht erlaubt.
		Wähle ein Schiff durch Anklicken, drehe es gegebenenfalls durch den entsprechenden Button. Ziehe es auf das Gewässer 
		und klicke dann auf die gewünschte Position um es abzusetzen. Wenn du ein Schiff wieder entfernen willst, kannst du es 
		durch Anklicken wieder zurücknehmen. Sind alle Schiffe eingesetzt, bestätige die Aufstellung und das Spiel kann gestartet werden.<br></p>

		<p><strong><FONT SIZE=4>Spielablauf</FONT></strong></p>

		<p>In dem oberen Gewässer ist das Feld des Gegners zu sehen. Hier wird pro Runde ein "Schuss" auf ein beliebiges Feld abgegeben um ein
		gegnerisches Schiff zu treffen. Das angeklickte Feld wird als bereits "beschossen" markiert. War der Schuss ein Treffer, wird dies
		angezeigt und ein neues Feld kann beschossen werden. Dies kann auch mehrmals hintereinander geschehen.<br>
		War der Schuss kein Treffer, so ist der Gegner an der Reihe.<br></p>
		
		<p><strong><FONT SIZE=4>Spielende</FONT></strong></p>
		
		<p>Gewonnen hat der Spieler, welcher zuerst alle Schiffe der gegnerischen Flotte versenkt hat.</p>
